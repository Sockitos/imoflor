CREATE VIEW public.rent_payments_view
  WITH (security_invoker = TRUE) AS
SELECT
  rp.contract_id,
  rp.id,
  m.value,
  m.occurred_at AS paid_at,
  m.description
FROM
  public.rent_payments rp
  JOIN public.movements m ON rp.movement_id = m.id;

CREATE VIEW public.installment_payments_view
  WITH (security_invoker = TRUE) AS
SELECT
  ip.contract_id,
  ip.id,
  i.occurred_at AS paid_at,
  i.value AS interest,
  COALESCE(a.value, 0::numeric) AS amortization,
  ip.extra_debt,
  i.description
FROM
  public.installment_payments ip
  LEFT JOIN public.movements a ON ip.amortization_movement_id = a.id
  LEFT JOIN public.movements i ON ip.interest_movement_id = i.id;

CREATE VIEW public.contracts_accounts_view
  WITH (security_invoker = TRUE) AS
SELECT
  dn.contract_id,
  dn.id,
  'due_note' AS type,
  (dn.due_date::timestamp AT TIME ZONE 'UTC') AS date,
  -1 * dn.value AS value
FROM
  public.due_notes dn
UNION
SELECT
  rp.contract_id,
  rp.id,
  'payment' AS type,
  rp.paid_at AS date,
  rp.value AS value
FROM
  public.rent_payments_view rp
UNION
SELECT
  ip.contract_id,
  ip.id,
  'payment' AS type,
  ip.paid_at AS date,
  ip.interest + ip.amortization AS value
FROM
  public.installment_payments_view ip;

CREATE VIEW public.contracts_balances_view
  WITH (security_invoker = TRUE) AS
SELECT
  c.id AS contract_id,
  COALESCE(sum(ca.value), 0) AS balance
FROM
  public.contracts c
  LEFT JOIN public.contracts_accounts_view ca ON c.id = ca.contract_id
GROUP BY
  c.id;

CREATE VIEW public.lending_contracts_debts_view
  WITH (security_invoker = TRUE) AS
SELECT
  lc.id,
  lc.sale_value - lc.down_payment - COALESCE(sum(ip.amortization), 0) AS debt,
  COALESCE((
    SELECT
      ip.extra_debt
    FROM public.installment_payments_view ip
    WHERE
      ip.contract_id = lc.id ORDER BY ip.paid_at DESC LIMIT 1), 0) AS extra_debt,
  max(ip.paid_at) AS last_paid_at
FROM
  public.lending_contracts lc
  LEFT JOIN public.installment_payments_view ip ON lc.id = ip.contract_id
GROUP BY
  lc.id;

CREATE VIEW public.lending_contracts_view
  WITH (security_invoker = TRUE) AS
SELECT
  lc.*,
  lcd.debt,
  lcd.extra_debt,
  lcd.last_paid_at,
  COALESCE(active_updates.installment, 0) AS installment,
  COALESCE(active_updates.interest, 0) AS interest,
  CASE WHEN last_updates.update_date IS NULL THEN
    NULL::json
  ELSE
    JSON_BUILD_OBJECT('update_date', last_updates.update_date, 'installment', COALESCE(last_updates.installment, 0), 'interest', COALESCE(last_updates.interest, 0))
  END AS next_update
FROM
  public.lending_contracts lc
  LEFT JOIN public.lending_contracts_debts_view lcd ON lc.id = lcd.id
  LEFT JOIN ( SELECT DISTINCT ON (iu.contract_id)
      iu.contract_id,
      iu.installment,
      iu.interest,
      iu.update_date
    FROM
      public.installment_updates iu
    ORDER BY
      iu.contract_id,
      iu.created_at DESC) AS last_updates ON lc.id = last_updates.contract_id
  LEFT JOIN ( SELECT DISTINCT ON (iu.contract_id)
      iu.contract_id,
      iu.installment,
      iu.interest,
      iu.update_date
    FROM
      public.installment_updates iu
    WHERE
      iu.update_date <= CURRENT_DATE
    ORDER BY
      iu.contract_id,
      iu.update_date DESC) AS active_updates ON lc.id = active_updates.contract_id;

CREATE VIEW public.renting_contracts_view
  WITH (security_invoker = TRUE) AS
SELECT
  rc.*,
  COALESCE(active_updates.rent, 0) AS rent,
  CASE WHEN next_updates.update_date IS NULL THEN
    NULL::json
  ELSE
    JSON_BUILD_OBJECT('update_date', next_updates.update_date, 'rent', next_updates.rent)
  END AS next_update
FROM
  public.renting_contracts rc
  LEFT JOIN ( SELECT DISTINCT ON (ru.contract_id)
      ru.contract_id,
      ru.rent,
      ru.update_date
    FROM
      public.rent_updates ru
    WHERE
      ru.update_date <= CURRENT_DATE
    ORDER BY
      ru.contract_id,
      ru.update_date DESC) AS active_updates ON rc.id = active_updates.contract_id
  LEFT JOIN ( SELECT DISTINCT ON (ru.contract_id)
      ru.contract_id,
      ru.rent,
      ru.update_date
    FROM
      public.rent_updates ru
    WHERE
      ru.update_date > CURRENT_DATE
    ORDER BY
      ru.contract_id,
      ru.created_at DESC) AS next_updates ON rc.id = next_updates.contract_id;

CREATE VIEW public.contracts_view
  WITH (security_invoker = TRUE) AS
SELECT
  c.*,
  cb.balance,
  CASE WHEN c.type = 'lending'::public.contract_type THEN
  (
    SELECT
      TO_JSON(lcv.*)
    FROM
      public.lending_contracts_view lcv
    WHERE
      lcv.id = c.id)
  WHEN c.type = 'renting'::public.contract_type THEN
  (
    SELECT
      TO_JSON(rcv.*)
    FROM
      public.renting_contracts_view rcv
    WHERE
      rcv.id = c.id)
  ELSE
    NULL::json
  END AS data
FROM
  public.contracts c
  LEFT JOIN public.contracts_balances_view cb ON c.id = cb.contract_id;

