SELECT
  cron.schedule('create-due-notes', '0 5 1 * *', $$ INSERT INTO public.due_notes(contract_id, due_date, value)
    SELECT
      c.id,
      date_trunc('month', CURRENT_DATE)::date,
      CASE WHEN c.type = 'renting'::public.contract_type THEN
        COALESCE(rcv.rent, 0)
      WHEN c.type = 'lending'::public.contract_type THEN
        COALESCE(lcv.installment, 0)
      ELSE
        0
      END
    FROM
      public.contracts c
      LEFT JOIN public.renting_contracts_view rcv ON c.type = 'renting'::public.contract_type
        AND rcv.id = c.id
      LEFT JOIN public.lending_contracts_view lcv ON c.type = 'lending'::public.contract_type
        AND lcv.id = c.id
    WHERE
      c.is_active ON CONFLICT ON CONSTRAINT uq_due_notes_contract_due_date DO NOTHING $$);

