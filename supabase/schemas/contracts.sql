CREATE TABLE public.contracts(
  id bigint GENERATED BY DEFAULT AS IDENTITY,
  created_at timestamp with time zone NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  created_by uuid REFERENCES public.users(id),
  updated_at timestamp with time zone NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_by uuid REFERENCES public.users(id),
  type public.contract_type NOT NULL,
  start_date date NOT NULL,
  end_date date,
  property_id bigint NOT NULL,
  is_active boolean NOT NULL DEFAULT TRUE,
  CONSTRAINT pk_contracts PRIMARY KEY (id),
  CONSTRAINT fk_contracts_property_id FOREIGN KEY (property_id) REFERENCES public.properties(id) ON DELETE CASCADE,
  CONSTRAINT uq_contracts_id_type UNIQUE (id, type),
  CONSTRAINT ck_contracts_end_date CHECK (end_date IS NULL OR end_date >= start_date)
);

CREATE UNIQUE INDEX idx_contracts_property_id_active
  ON public.contracts(property_id)
  WHERE is_active;

